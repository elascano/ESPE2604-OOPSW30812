    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        try {
            // Read values from text fields
            System.out.println("El botón fue presionado");
            int id = Integer.parseInt(txtId.getText());
            String name = txtName.getText();
            String description = txtDescription.getText();
            double power = Double.parseDouble(txtPower.getText());
            double impedance = Double.parseDouble(txtImpedance.getText());

            // Business rule validation
            if (power <= 0 || impedance <= 0) {
                lblResult.setText("Error: Power and Impedance must be greater than 0");
                return;
            }

            // Call controller to create speaker
            controller.createSpeaker(id, name, description, power, impedance);

            // Calculate and show performance value
            double performance = power / impedance;
            lblResult.setText("Product created successfully! Performance: " + String.format("%.2f", performance) + " | Total: " + controller.getTotalCount());
            
            controller.showAllSpeakers();

            // Clear all text fields
            txtId.setText("");
            txtName.setText("");
            txtDescription.setText("");
            txtPower.setText("");
            txtImpedance.setText("");

        } catch (NumberFormatException e) {
            lblResult.setText("Error: Please enter valid numbers");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

