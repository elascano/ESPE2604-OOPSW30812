    private void loadAnimalsTable() {

        AnimalController controller = new AnimalController();
        ArrayList<FarmAnimal> animals = controller.read();

        DefaultTableModel model = new DefaultTableModel();

        model.addColumn("ID");
        model.addColumn("Type");
        model.addColumn("Breed");
        model.addColumn("Birth");
        model.addColumn("Weight");

        for (FarmAnimal animal : animals) {

            model.addRow(new Object[]{
                animal.getId(),
                animal.getClass().getSimpleName(),
                animal.getBreed(),
                animal.getBornOnDate(),
                animal.getWeight()

            });

        }
        tblAnimals.setModel(model);

    }

    private void loadAnimal(FarmAnimal animal) {

        txtId.setText(String.valueOf(animal.getId()));
        txtBreed.setText(animal.getBreed());
        txtWeight.setText(String.valueOf(animal.getWeight()));
        txtBornOnDate.setText(animal.getBornOnDate().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));

        if (animal instanceof Chicken) {

            Chicken chicken = (Chicken) animal;

            cmbType.setSelectedItem("Chicken");
            cbxIsMolting.setSelected(chicken.isMolting());
            sldNumberOfEggs.setValue(chicken.getNumberOfEggs());

        } else if (animal instanceof Pig) {

            Pig pig = (Pig) animal;

            cmbType.setSelectedItem("Pig");
            txtIdealWeight.setText(String.valueOf(pig.getIdealWeight()));

        } else if (animal instanceof Cow) {

            Cow cow = (Cow) animal;

            cmbType.setSelectedItem("Cow");
            cbxIsProducingMilk.setSelected(cow.isIsProducingMilk());

        } else if (animal instanceof Sheep) {

            Sheep sheep = (Sheep) animal;

            cmbType.setSelectedItem("Sheep");
            txtLastShearing.setText(sheep.getLastShearing().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
        }

    }

    private FarmAnimal createAnimalFromForm() {

        String type = cmbType.getSelectedItem().toString();

        int id = Integer.parseInt(txtId.getText());
        String breed = txtBreed.getText();
        float weight = Float.parseFloat(txtWeight.getText());

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("MM/dd/yyyy");
        LocalDate bornOnDate = LocalDate.parse(txtBornOnDate.getText(), formatter);

        switch (type) {

            case "Chicken":
                return new Chicken(id, breed, bornOnDate, weight, cbxIsMolting.isSelected(), sldNumberOfEggs.getValue());

            case "Cow":
                return new Cow(id, breed, bornOnDate, weight, cbxIsProducingMilk.isSelected());

            case "Pig":
                float idealWeight = Float.parseFloat(txtIdealWeight.getText());
                return new Pig(id, breed, bornOnDate, weight, idealWeight);

            case "Sheep":
                LocalDate lastShearing = LocalDate.parse(txtLastShearing.getText(), formatter);
                return new Sheep(id, breed, bornOnDate, weight, lastShearing);

            default:
                return null;
        }

    }

