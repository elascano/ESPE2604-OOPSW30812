    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int id = Integer.parseInt(txtId.getText());
        AnimalController controller = new AnimalController();

        if (controller.delete(id)) {
            JOptionPane.showMessageDialog(this, "Animal deleted.");
            loadAnimalsTable();
            clear();

        } else {
            JOptionPane.showMessageDialog(this, "Animal was not deleted");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblAnimalsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAnimalsMouseClicked
        int row = tblAnimals.getSelectedRow();

        if (row == -1) {
            return;
        }

        int id = Integer.parseInt(tblAnimals.getValueAt(row, 0).toString());
        AnimalController controller = new AnimalController();
        FarmAnimal animal = controller.findById(id);

        if (animal != null) {
            loadAnimal(animal);
        }

    }//GEN-LAST:event_tblAnimalsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        FarmAnimal animal = createAnimalFromForm();
        AnimalController controller = new AnimalController();

        if (controller.update(animal)) {
            JOptionPane.showMessageDialog(this, "Animal updated successfully.");
            loadAnimalsTable();
            clear();

        } else {
            JOptionPane.showMessageDialog(this, "Animal was not updated.");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

