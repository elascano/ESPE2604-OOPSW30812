    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        AnimalController controller = new AnimalController();
        FarmAnimal animal = createAnimalFromForm();

        if (animal == null) {
            return;
        }

        if (controller.create(animal)) {

            JOptionPane.showMessageDialog(this, "Animal saved successfully.");
            loadAnimalsTable();
            clear();

        } else {
            JOptionPane.showMessageDialog(this, "An animal with ID " + animal.getId() + " already exists.");

        }
    }//GEN-LAST:event_btnSaveActionPerformed

