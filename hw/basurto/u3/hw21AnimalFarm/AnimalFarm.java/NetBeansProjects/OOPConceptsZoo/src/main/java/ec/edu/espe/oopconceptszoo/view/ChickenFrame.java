    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    try {
        int id = (int) spnId.getValue();
        String breed = cmbBreed.getSelectedItem().toString();
        float weight = Float.parseFloat(txtWeight.getText());
        boolean isMolting = chkMolting.isSelected();
        int eggs = (int) spnEggs.getValue();

        ec.edu.espe.oopconceptszoo.model.Chicken chicken = new ec.edu.espe.oopconceptszoo.model.Chicken(
            id, breed, new java.util.Date(), weight, isMolting, eggs
        );
        
        chicken.saveToDatabase();
        
        boolean eligible = !isMolting && eggs > 2;
        lblResult.setText("Status: " + (eligible ? "Optimal Production" : "Low Efficiency"));
        
        javax.swing.JOptionPane.showMessageDialog(this, "Chicken data synchronized and saved to Atlas!");
    } catch(Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Input Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveActionPerformed

