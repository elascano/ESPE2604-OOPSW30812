    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
try {
        int id = (int) spnId.getValue();
        com.mongodb.client.MongoDatabase db = ec.edu.espe.oopconceptszoo.controller.MongoConnection.getDatabase();
        com.mongodb.client.MongoCollection<org.bson.Document> collection = db.getCollection("chickens");
        
        // Busca por el campo "id" que guardaste
        org.bson.Document result = collection.findOneAndDelete(new org.bson.Document("id", id));
        
        if (result != null) {
            lblResult.setText("Status: Record Deleted");
            javax.swing.JOptionPane.showMessageDialog(this, "Chicken with ID " + id + " deleted from Atlas.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "ID not found.", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

