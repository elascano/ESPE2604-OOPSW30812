    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
    String username = txtUser.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();

    
    if (username.isEmpty() || password.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields", "Warning", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        
        com.mongodb.client.MongoDatabase db = ec.edu.espe.oopconceptszoo.controller.MongoConnection.getDatabase();
        
        if (db != null) {
            
            com.mongodb.client.MongoCollection<org.bson.Document> collection = db.getCollection("Users");
            
            org.bson.Document query = new org.bson.Document("username", username).append("password", password);
            org.bson.Document userFound = collection.find(query).first();

           
            if (userFound != null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Welcome to the Farm System!");
                
                
                new MenuFrame().setVisible(true);
                this.dispose();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Invalid username or password", "Login Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Could not connect to MongoDB Atlas", "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error during authentication: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnLoginActionPerformed

