    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    try {
        int id = (int) spnId.getValue();
        String breed = cmbBreed.getSelectedItem().toString();
        float weight = Float.parseFloat(txtWeight.getText());
        float idealWeight = Float.parseFloat(txtIdealWeight.getText());

        ec.edu.espe.oopconceptszoo.model.Pig pig = new ec.edu.espe.oopconceptszoo.model.Pig(
            id, breed, new java.util.Date(), weight, idealWeight
        );
        
        pig.saveToDatabase();
        
        double expectedMeat = (weight * 0.15) + (weight * 0.10);
        lblMeatResult.setText(String.format("%.2f kg", expectedMeat));
        
        javax.swing.JOptionPane.showMessageDialog(this, "Pig record saved to Atlas!");
    } catch(Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Input Error: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveActionPerformed

