    public void loadAnimalTable() {

        AnimalController controller = new AnimalController();
        ArrayList<FarmAnimal> animals = controller.read();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Type");
        model.addColumn("Breed");
        model.addColumn("Birth");
        model.addColumn("Weight");

        for (FarmAnimal animal : animals) {

            model.addRow(new Object[]{
                animal.getId(),
                animal.getClass().getSimpleName(),
                animal.getBreed(),
                animal.getBornOnDate(),
                animal.getWeight()

            });

        }

        tblAnimals.setModel(model);
    }

    public void loadCutsTable() {
        CutController controller = new CutController();
        ArrayList<Cut> cuts = controller.read();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Description");
        model.addColumn("Procedure");
        model.addColumn("Weight");

        for (Cut cut : cuts) {
            model.addRow(new Object[]{
                cut.getDescription(),
                cut.getProcedure(),
                cut.getWeight()
            });
        }

        tblCuts.setModel(model);

    }

    public void loadAnimal() {

        AnimalController controller = new AnimalController();
        int id = Integer.parseInt(lblId.getText());

        FarmAnimal animal = controller.findById(id);

        if (animal instanceof Chicken) {

            Chicken chicken = (Chicken) animal;
            int numberOfEggs = chicken.getNumberOfEggs();

            lblNumerOfEggs.setText(String.valueOf(numberOfEggs));
            if (chicken.isMolting()) {
                lblIsMolting.setText("True");
            } else {
                lblIsMolting.setText("False");
            }

        } else if (animal instanceof Cow) {
            Cow cow = (Cow) animal;
            if (cow.isIsProducingMilk()) {
                lblIsProducingMilk.setText("True");
            } else {
                lblIsProducingMilk.setText("False");
            }
        } else if (animal instanceof Sheep) {
            Sheep sheep = (Sheep) animal;
            LocalDate lastShearing = sheep.getLastShearing();
            lblLastShearing.setText(lastShearing.toString());
        } else if (animal instanceof Pig) {
            Pig pig = (Pig) animal;
            float weight = pig.getWeight();
            float idealWeight = pig.getIdealWeight();

            lblIdealWeight.setText(String.valueOf(idealWeight));
            lblWeight.setText(String.valueOf(weight));
        }

    }

    private void loadFoodComboBox() {

        cmbFood.removeAllItems();

        FoodController controller = new FoodController();

        ArrayList<Food> foods = controller.read();

        for (Food food : foods) {
            cmbFood.addItem(food);
        }

    }

