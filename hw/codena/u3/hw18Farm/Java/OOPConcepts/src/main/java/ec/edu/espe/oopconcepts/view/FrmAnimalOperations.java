    private void btnLayAnEggActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLayAnEggActionPerformed

        if (lblId.getText().equals("-")) {
            JOptionPane.showMessageDialog(this, "Select a chicken");
            return;
        }

        int id = Integer.parseInt(lblId.getText());

        AnimalController controller = new AnimalController();
        FarmAnimal animal = controller.findById(id);
        Chicken chicken = (Chicken) animal;

        if (chicken.layAnEgg()) {
            JOptionPane.showMessageDialog(this, "Chicken " + lblId.getText() + " cannot lay an egg");
        } else {
            controller.update(chicken);
            JOptionPane.showMessageDialog(this, "Chicken " + lblId.getText() + " layed an egg");
            loadAnimal();
            loadAnimalTable();
        }
    }//GEN-LAST:event_btnLayAnEggActionPerformed

    private void btnCutPigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCutPigActionPerformed
        if (lblId.getText().equals("-")) {
            JOptionPane.showMessageDialog(this, "Select a pig");
            return;
        }

        AnimalController animalController = new AnimalController();
        CutController cutController = new CutController();

        int id = Integer.parseInt(lblId.getText());
        FarmAnimal animal = animalController.findById(id);
        Pig pig = (Pig) animal;

        if (pig.sendToButcher()) {
            ArrayList<Cut> cuts = pig.cut();

            for (Cut cut : cuts) {
                cutController.create(cut);
            }

            animalController.delete(id);
            JOptionPane.showMessageDialog(this, "Pig " + String.valueOf(id) + " died, 3 new cuts have been added");
            loadAnimalTable();
            loadCutsTable();
            clear();
        } else {
            JOptionPane.showMessageDialog(this, "Pig has not been sent to butcher, The weight is lower than the ideal weight");
        }


    }//GEN-LAST:event_btnCutPigActionPerformed

