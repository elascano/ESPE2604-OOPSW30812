    private void btnFeedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFeedActionPerformed
        if (lblId.getText().equals("-")) {
            JOptionPane.showMessageDialog(this, "Select an Animal");
            return;
        }
        AnimalController animalController = new AnimalController();
        FoodController foodController = new FoodController();

        int id = Integer.parseInt(lblId.getText());
        Food selectedFood = (Food) cmbFood.getSelectedItem();
        FarmAnimal animal = animalController.findById(id);

        if (animal.feed(selectedFood)) {
            animalController.update(animal);
            foodController.delete(selectedFood.getId());

            loadAnimalTable();
            loadAnimal();
            loadFoodComboBox();
        } else {
            JOptionPane.showMessageDialog(this, "You cannot feed this animal with " + selectedFood.getTypeOfFood());
        }

    }//GEN-LAST:event_btnFeedActionPerformed

