    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        javax.swing.table.DefaultTableModel tableModel = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        tableModel.setRowCount(0);

        String selection = jComboBox1.getSelectedItem().toString();
        com.mongodb.client.MongoDatabase database = Utils.MongoDBConnection.getDatabase();

        if (selection.equals("Customer")) {
            tableModel.setColumnIdentifiers(new Object[]{"RUC", "Name", "Address", "Gmail"});
            com.mongodb.client.MongoCollection<org.bson.Document> collection = database.getCollection("customers");
            for (org.bson.Document doc : collection.find()) {
                tableModel.addRow(new Object[]{doc.get("ruc"), doc.get("name"), doc.get("address"), doc.get("gmailCustomer")});
            }
        } else if (selection.equals("Produc")) {
            tableModel.setColumnIdentifiers(new Object[]{"ID", "Name", "Unit Price", "Stock"});
            com.mongodb.client.MongoCollection<org.bson.Document> collection = database.getCollection("products");
            for (org.bson.Document doc : collection.find()) {
                tableModel.addRow(new Object[]{doc.get("id"), doc.get("name"), doc.get("unitPrice"), doc.get("stock")});
            }
        } else if (selection.equals("Supplier")) {
            tableModel.setColumnIdentifiers(new Object[]{"RUC", "Company", "Address", "Phone", "Email"});
            com.mongodb.client.MongoCollection<org.bson.Document> collection = database.getCollection("suppliers");
            for (org.bson.Document doc : collection.find()) {
                tableModel.addRow(new Object[]{doc.get("ruc"), doc.get("companyName"), doc.get("address"), doc.get("phone"), doc.get("email")});
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

