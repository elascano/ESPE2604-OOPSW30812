    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        CutController controller = new CutController();
        int id = Integer.parseInt(txtId.getText());
        String desc = txtDescription.getText();
        String proc = txtProcedure.getText();
        float weight = ((Number) spnWeight.getValue()).floatValue();

        
        if (controller.saveCut(this.animalId, id, desc, proc, weight)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cut saved to MongoDB!");
            loadTableData();
            clearFields();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        float weight = ((Number) spnWeight.getValue()).floatValue();
        if (this.parentForm != null) {
            this.parentForm.setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblCuts.getSelectedRow();

        if (selectedRow != -1) {
           
            int idToDelete = (int) tblCuts.getValueAt(selectedRow, 0);

            CutController controller = new CutController();
            if (controller.deleteCut(idToDelete)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Cut deleted successfully!");
                loadTableData(); 
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error deleting the cut.");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a cut from the table.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    private void loadTableData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblCuts.getModel();
        model.setRowCount(0);
        CutController controller = new CutController();
        for (Cut cut : controller.getCutsByAnimal(this.animalId)) {
            model.addRow(new Object[]{cut.getId(), cut.getDescription(), cut.getProcedure(), cut.getWeight()});
        }
    }

    private void clearFields() {
        txtId.setText("");
        txtDescription.setText("");
        txtProcedure.setText("");
        spnWeight.setValue(0);
    }

