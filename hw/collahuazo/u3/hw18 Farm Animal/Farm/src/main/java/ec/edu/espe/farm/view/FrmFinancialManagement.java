    private void btnRegisterSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterSaleActionPerformed
        float amount = Float.parseFloat(txtAmount.getText());
        FarmFinances finances = new FarmFinances();
        finances.setTotalRevenue(amount);
        finances.setTotalExpenses(0);
        FinanceController controller = new FinanceController();
        if (controller.saveFinances(finances)) {
            JOptionPane.showMessageDialog(this, "Sale recorded");
            refreshData();
        }
    }//GEN-LAST:event_btnRegisterSaleActionPerformed

    private void btnRegisterExpenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterExpenseActionPerformed
        float amount = Float.parseFloat(txtAmount.getText());
        FarmFinances finances = new FarmFinances();
        finances.setTotalRevenue(0);
        finances.setTotalExpenses(amount);

        FinanceController controller = new FinanceController();
        if (controller.saveFinances(finances)) {
            JOptionPane.showMessageDialog(this, "Recorded expense");
            refreshData();
        }
    }

    private void refreshData() {
        FinanceController controller = new FinanceController();
        java.util.List<org.bson.Document> history = controller.getFinanceHistory();
        double totalRevenue = 0;
        double totalExpenses = 0;

        for (org.bson.Document doc : history) {
            totalRevenue += doc.getDouble("totalRevenue");
            totalExpenses += doc.getDouble("totalExpenses");
        }

        double netProfit = totalRevenue - totalExpenses;

        txtTotalRevenue.setText(String.format("%.2f", totalRevenue));
        txtTotalCosts.setText(String.format("%.2f", totalExpenses));
        txtNetProfit.setText(String.format("%.2f", netProfit));
    }

