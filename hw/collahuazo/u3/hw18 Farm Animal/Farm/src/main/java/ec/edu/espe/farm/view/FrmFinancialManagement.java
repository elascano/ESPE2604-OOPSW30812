    private void updateNetProfit() {
        try {
            double revenue = Double.parseDouble(txtTotalRevenue.getText().replace(",", "."));
            double costs = Double.parseDouble(txtTotalCosts.getText().replace(",", "."));
            double profit = revenue - costs;
            txtNetProfit.setText(String.format("%.2f", profit));
        } catch (NumberFormatException e) {
            txtNetProfit.setText("0.00");
        }
    }

