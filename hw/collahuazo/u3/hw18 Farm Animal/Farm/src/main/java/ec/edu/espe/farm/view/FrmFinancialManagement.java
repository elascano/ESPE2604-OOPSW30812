    private void btnProcessBalanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessBalanceActionPerformed
        FarmAnimal selectedAnimal = (FarmAnimal) cmbAnimals.getSelectedItem();
        if (selectedAnimal != null) {
            FarmFinances finances = new FarmFinances();
            finances.processAnimalBalance(selectedAnimal);
            JOptionPane.showMessageDialog(this, "Balance processed correctly for: " + selectedAnimal.getBreed());
            refreshData(); 
        } else {
            JOptionPane.showMessageDialog(this, "Please select an animal");
        }
    }//GEN-LAST:event_btnProcessBalanceActionPerformed
    
    private void cmbAnimalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbAnimalsActionPerformed
        FarmAnimal selectedAnimal = (FarmAnimal) cmbAnimals.getSelectedItem();
        if (selectedAnimal != null) {
            FarmAnimalController controller = new FarmAnimalController();
         
            int ageInMonths = controller.calculateAgeInMonths(selectedAnimal);

            if (ageInMonths > 6) {
                lblProductionStatus.setText("Status: Production ready");
            } else {
                lblProductionStatus.setText("Status: In development");
            }
        }
    }
     private void loadAnimals() {
        FarmAnimalController animalController = new FarmAnimalController();
        java.util.List<FarmAnimal> animals = animalController.getAnimals(); 
        
        cmbAnimals.removeAllItems(); 
        for (FarmAnimal animal : animals) {
            cmbAnimals.addItem(animal);
        }
    
    }//GEN-LAST:event_cmbAnimalsActionPerformed

