    private void btnSaveAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAppointmentActionPerformed
try {
        String motherId = txtId.getText().trim(); 
        String appointmentDate = txtAppointmentDate.getText().trim();
        String recommendations = txtRecommendations.getText().trim();

        if (motherId.isEmpty() || appointmentDate.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(
                null, 
                "Please enter the mother's ID number and the appointment date.", 
                "Incomplete fields", 
                javax.swing.JOptionPane.WARNING_MESSAGE
            );
            return;
        }


        var database = ec.edu.espe.mothersApp.model.MongoConnection.getDatabase();
        var collection = database.getCollection("appointments");

        String appointmentId = motherId + "_" + appointmentDate.replace("/", "-");

        org.bson.Document doc = new org.bson.Document("_id", appointmentId)
                            .append("motherId", motherId)
                            .append("appointmentDate", appointmentDate)
                            .append("recommendations", recommendations);

        var filter = com.mongodb.client.model.Filters.eq("_id", appointmentId);
        var options = new com.mongodb.client.model.ReplaceOptions().upsert(true);
        collection.replaceOne(filter, doc, options);

        javax.swing.JOptionPane.showMessageDialog(
            null, 
            "Appointment reminder successfully scheduled and uploaded to the cloud!", 
            "Saved Appointment", 
            javax.swing.JOptionPane.INFORMATION_MESSAGE
        );
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(
            null, 
            "Error scheduling appointment: " + e.getMessage(), 
            "Error", 
            javax.swing.JOptionPane.ERROR_MESSAGE
        );
        
    }    }//GEN-LAST:event_btnSaveAppointmentActionPerformed
