    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtId.getText().trim().isEmpty() || txtName.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Fields cannot be empty.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int id = Integer.parseInt(txtId.getText().trim());
    
        com.mongodb.client.MongoDatabase db = com.mongodb.client.MongoClients.create("mongodb+srv://Angie:Angie@angienx.spphrbg.mongodb.net/MothersAppDB?retryWrites=true&w=majority").getDatabase("MothersAppDB");

        org.bson.Document doctorData = new org.bson.Document()
            .append("id", id)
            .append("name", txtName.getText().trim())
            .append("speciality", cmbSpeciality.getSelectedItem().toString());

        org.bson.Document finalJson = new org.bson.Document()
            .append("module", "Doctor Registration")
            .append("syncDate", new java.util.Date().toString())
            .append("doctorData", doctorData);

        db.getCollection("allProjectRecords").insertOne(finalJson);
        javax.swing.JOptionPane.showMessageDialog(this, "Doctor data saved directly to Cloud!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
    
        txtId.setText("");
        txtName.setText("");
    
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "ID must be a valid integer number.", "Type Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Cloud Error: " + e.getMessage(), "Connection Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveActionPerformed

