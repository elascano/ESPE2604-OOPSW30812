// Método para dibujar las burbujas estilo WhatsApp
    private void agregarMensajeAlChat(String remitente, String mensaje) {
        String burbuja = " ";
        
        if (remitente.equalsIgnoreCase("Mother")) {
            burbuja = "<div style='text-align: right; margin-bottom: 10px;'>"
                    + "<span style='background-color: #d9fdd3; padding: 8px 14px; "
                    + "border-radius: 12px; display: inline-block; max-width: 70%; text-align: left; "
                    + "border: 1px solid #c2ebd0; color: #111b21;'> "
                    + "<b>Mother:</b> " + mensaje
                    + "</span></div>";
        } else {
            burbuja = "<div style='text-align: left; margin-bottom: 10px;'>"
                    + "<span style='background-color: #ffffff; padding: 8px 14px; "
                    + "border-radius: 12px; display: inline-block; max-width: 70%; "
                    + "border: 1px solid #e1e4e8; color: #111b21;'>"
                    + "<b>Chat:</b> " + mensaje
                    + "</span></div>";
        }
        
        historialChat += burbuja;
        jTextPane1.setText(historialChat + "</body></html>");
       
    }
    
    private void clickSend() {
        String mensaje = javax.swing.JOptionPane.showInputDialog(this, "Escribe tu consulta de mamá:");
        if (mensaje != null && !mensaje.trim().isEmpty()) {
            agregarMensajeAlChat("Mother", mensaje);
        }
    }

    private void clickReply() {
        String respuesta = javax.swing.JOptionPane.showInputDialog(this, "Respuesta del Pediatra / Sistema:");
        if (respuesta != null && !respuesta.trim().isEmpty()) {
            agregarMensajeAlChat("Sistema", respuesta);
        }
    }
