    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        try {
        int day = Integer.parseInt(cmbDay.getSelectedItem().toString());
        int year = Integer.parseInt(cmbYear.getSelectedItem().toString());
        
        int monthIndex = cmbMonth.getSelectedIndex(); 

        java.util.Calendar fum = java.util.Calendar.getInstance();
        fum.set(year, monthIndex, day, 0, 0, 0);
        fum.set(java.util.Calendar.MILLISECOND, 0);

        java.util.Calendar hoy = java.util.Calendar.getInstance();
        hoy.set(java.util.Calendar.HOUR_OF_DAY, 0);
        hoy.set(java.util.Calendar.MINUTE, 0);
        hoy.set(java.util.Calendar.SECOND, 0);
        hoy.set(java.util.Calendar.MILLISECOND, 0);

        if (fum.after(hoy)) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "La fecha de la última menstruación no puede ser una fecha futura.", 
                "Error de Fecha", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        long diferenciaMilisegundos = hoy.getTimeInMillis() - fum.getTimeInMillis();
        long diasTranscurridos = diferenciaMilisegundos / (24 * 60 * 60 * 1000);

        long semanas = diasTranscurridos / 7;
        long diasRestantes = diasTranscurridos % 7;

        if (semanas > 42) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "El cálculo excede las 42 semanas de gestación. Por favor verifique la fecha ingresada.", 
                "Aviso Médico", javax.swing.JOptionPane.WARNING_MESSAGE);
        }

        java.util.Calendar fpp = (java.util.Calendar) fum.clone();
        fpp.add(java.util.Calendar.DAY_OF_YEAR, 280);

        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        String fechaPartoFormateada = sdf.format(fpp.getTime());

        // 6. Mostrar los resultados en la interfaz
        lblWeeksResult.setText("Semanas de Gestación: " + semanas + " semanas y " + diasRestantes + " días.");
        lblDueDateResult.setText("Fecha Probable de Parto (FPP): " + fechaPartoFormateada);

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, 
            "Ocurrió un error al procesar las fechas. Asegúrese de que sea una fecha válida.", 
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCalculateActionPerformed

