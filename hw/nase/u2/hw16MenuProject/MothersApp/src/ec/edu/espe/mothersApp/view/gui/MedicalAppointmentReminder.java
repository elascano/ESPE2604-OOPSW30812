    private void btnSaveAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAppointmentActionPerformed
        String recommendations = txtRecommendations.getText().trim();

    // 2. Validar que no se guarde un recordatorio vacío
    if (recommendations.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Por favor, ingrese las recomendaciones o indicaciones de la cita.", 
            "Campo Vacío", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    // 3. Capturar la fecha seleccionada usando tus tres ComboBoxes de image_f1b81f.png
    String day = jComboBox1.getSelectedItem().toString();
    String month = jComboBox2.getSelectedItem().toString();
    String year = jComboBox3.getSelectedItem().toString();
    String appointmentDate = day + " de " + month + " del " + year;

    // 4. Mostrar cuadro de confirmación con los datos estructurados
    String mensajeExito = "¡Recordatorio de Cita Médica Agendado!\n\n"
                        + "Fecha de la Cita: " + appointmentDate + "\n"
                        + "Recomendaciones: " + recommendations;
                        
    JOptionPane.showMessageDialog(this, mensajeExito, "Recordatorio Guardado", JOptionPane.INFORMATION_MESSAGE);

    // 5. Limpiar el cuadro de texto para el próximo registro
    txtRecommendations.setText("");
    }//GEN-LAST:event_btnSaveAppointmentActionPerformed

