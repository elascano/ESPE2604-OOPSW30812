    private void btnSaveMotherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveMotherActionPerformed
String firstName = txtFirstName.getText().trim();
    String lastName = txtLastName.getText().trim();
    String id = txtId.getText().trim();


    if (firstName.isEmpty() || lastName.isEmpty() || id.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Por favor, llene todos los campos de identidad obligatorios.", 
            "Campos Incompletos", 
            JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (id.length() != 10 || !id.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, 
            "Error: La cédula (Id) debe contener exactamente 10 dígitos numéricos.", 
            "Validación de Identidad", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }

    String month = btmMonth.getSelectedItem().toString();
    String year = btmYear.getSelectedItem().toString();
    String birthDate = month + " / " + year;

    String mensajeExito = "¡Madre registrada con éxito!\n"
                        + "Nombre: " + firstName + " " + lastName + "\n"
                        + "Cédula: " + id + "\n"
                        + "Fecha: " + birthDate;
                        
    JOptionPane.showMessageDialog(this, mensajeExito, "Registro Completado", JOptionPane.INFORMATION_MESSAGE);

    txtFirstName.setText("");
    txtLastName.setText("");
    txtId.setText("");

    }//GEN-LAST:event_btnSaveMotherActionPerformed

