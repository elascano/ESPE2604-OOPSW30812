import random

def run_addition_test(num1, num2, user_answer, test_id):
    correct_answer = num1 + num2
    if user_answer == correct_answer:
        print(f"Test {test_id}: {num1} + {num2} = {user_answer} (Correcto)")
    else:
        print(f"Test {test_id}: {num1} + {num2} = {user_answer} (Incorrecto). La respuesta correcta era {correct_answer}")

print("\n--- 5 operaciones correctas  ---")

for i in range(1, 6):
    num1 = random.randint(1, 25)
    num2 = random.randint(1, 50)
    correct_answer = num1 + num2
    run_addition_test(num1, num2, correct_answer, i)

def run_subtraction_test(num1, num2, user_answer, test_id):
    correct_answer = round(num1 - num2, 2)
    if user_answer == correct_answer:
        print(f"Test {test_id}: {num1} - {num2} = {user_answer} (Correcto)")
    else:
        print(f"Test {test_id}: {num1} - {num2} = {user_answer} (Incorrecto). La respuesta correcta era {correct_answer}")

print("\n--- 5 operaciones correctas ---")

for i in range(1, 6):
    num1 = round(random.uniform(25, 50), 2)
    num2 = round(random.uniform(1, 25), 2)
    correct_answer = round(num1 - num2, 2)
    run_subtraction_test(num1, num2, correct_answer, i)

def run_multiplication_test(num1, num2, user_answer, test_id):
    correct_answer = round(num1 * num2, 2)
    if user_answer == correct_answer:
        print(f"Test {test_id}: {num1} * {num2} = {user_answer} (Correcto)")
    else:
        print(f"Test {test_id}: {num1} * {num2} = {user_answer} (Incorrecto). La respuesta correcta era {correct_answer}")

print("\n--- 5 operaciones correctas ---")

for i in range(1, 6):
    num1 = round(random.uniform(1, 10), 2)
    num2 = round(random.uniform(1, 10), 2)
