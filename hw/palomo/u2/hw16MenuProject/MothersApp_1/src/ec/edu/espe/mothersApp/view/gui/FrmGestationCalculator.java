    private void rdoMethodChanged(java.awt.event.ActionEvent evt) {
        updateInputFields();
    }
    
    private void updateInputFields() {
        boolean byLMP = rdoByLMP.isSelected();
        lblLMPdate.setVisible(byLMP);
        txtLmpDate.setVisible(byLMP);
        lblCurrentweek1.setVisible(!byLMP);
        spnCurrentWeek.setVisible(!byLMP);
    }
    
    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        try {
            GestationResult result;
            if (rdoByLMP.isSelected()) {
                String lmp = txtLmpDate.getText().trim();
                if (lmp.isEmpty()) {
                    showStatus("⚠ Please enter the LMP date (YYYY-MM-DD).", false);
                    return;
                }
                result = GestationCalculator.calculateFromLMP(lmp);
            } else {
                int week = (Integer) spnCurrentWeek.getValue();
                result = GestationCalculator.calculateFromWeek(week);
            }
 
            lblResultWeek.setText("Week " + result.currentWeek + " of 40");
            lblResultTrimester.setText(result.trimester);
            lblResultDueDate.setText(result.estimatedDueDate);
 
            java.awt.Color color;
            if (result.currentWeek <= 12)      color = new java.awt.Color(59, 109, 17);
            else if (result.currentWeek <= 27) color = new java.awt.Color(133, 79, 11);
            else                               color = new java.awt.Color(163, 45, 45);
 
            lblResultWeek.setForeground(color);
            lblResultTrimester.setForeground(color);
            lblResultDueDate.setForeground(color);
 
            lastResult = result;
            btnSavetoMongoDB.setEnabled(true);
            showStatus("✔ Calculation complete. You can now save to MongoDB.", true);
 
        } catch (Exception e) {
            showStatus("⚠ Invalid date format. Use YYYY-MM-DD (e.g. 2024-04-15).", false);
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

    private void btnSaveMongoActionPerformed(java.awt.event.ActionEvent evt) {
        if (lastResult == null) {
            showStatus("⚠ Please calculate first.", false);
            return;
        }
        String motherId = txtMotherId.getText().trim();
        if (motherId.isEmpty()) motherId = "unknown";
        GestationCalculator.saveToMongo(motherId, lastResult);
        showStatus("✔ Record saved to MongoDB successfully.", true);
        btnSavetoMongoDB.setEnabled(false);
    }

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {                                         
        txtMotherId.setText("");
        txtLmpDate.setText("");
        spnCurrentWeek.setValue(1);
        rdoByLMP.setSelected(true);
        updateInputFields();
        clearResults();
        lastResult = null;
        btnSavetoMongoDB.setEnabled(false);
    }
    
    private void btnBackMenuActionPerformed(java.awt.event.ActionEvent evt) {                                            
        this.dispose();
    }
    
    private void clearResults() {
        lblResultWeek.setText("-");
        lblResultTrimester.setText("-");
        lblResultDueDate.setText("-");
        lblStatus.setText("");
        lblStatus.setOpaque(false);
        lblStatus.setBorder(null);
    }
    
    private void showStatus(String msg, boolean success) {
        lblStatus.setText(msg);
        if (success) {
            lblStatus.setForeground(new java.awt.Color(59, 109, 17));
            lblStatus.setBackground(new java.awt.Color(234, 243, 222));
        } else {
            lblStatus.setForeground(new java.awt.Color(133, 79, 11));
            lblStatus.setBackground(new java.awt.Color(250, 238, 218));
        }
        lblStatus.setOpaque(true);
        lblStatus.setBorder(javax.swing.BorderFactory.createEmptyBorder(4, 10, 4, 10));
    }
    
