    private void btnSaveMotherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveMotherActionPerformed
    try {
        String babyFirstName = txtBabyFirstName.getText().trim();
        String babyLastName = txtBabyLastName.getText().trim();
        int babyWeight = Integer.parseInt(txtBabyWeight.getText().trim());
        int babyHeight = Integer.parseInt(txtBabyHeight.getText().trim());
        String babyBirthDate = txtBabyBirthDate.getText().trim();
        
        String motherId = txtId.getText().trim(); 
        String motherFirstName = txtFirstName.getText().trim();
        String motherLastName = txtLastName.getText().trim();
        String motherBirthDate = txtBirthDate.getText().trim();
        double motherWeight = Double.parseDouble(txtWeight.getText().trim());
        double motherHeight = Double.parseDouble(txtHeight.getText().trim());

        Baby baby = new Baby(babyFirstName, babyLastName, "", babyWeight, babyHeight, babyBirthDate, true);
        Mother mother = new Mother(motherFirstName, motherLastName, motherId, motherBirthDate, motherWeight, motherHeight);
        
        mother.babies = new java.util.ArrayList<>();
        mother.babies.add(baby);

        ec.edu.espe.mothersApp.model.MedicalDataBase db = new ec.edu.espe.mothersApp.model.MedicalDataBase();
        db.saveHistory(mother);

        var database = ec.edu.espe.mothersApp.model.MongoConnection.getDatabase();
        var collection = database.getCollection("Mother"); 

        org.bson.Document babyDoc = new org.bson.Document("firstName", babyFirstName)
                                    .append("lastName", babyLastName)
                                    .append("weight", babyWeight)
                                    .append("height", babyHeight)
                                    .append("birthDate", babyBirthDate);

        org.bson.Document doc = new org.bson.Document("firstName", motherFirstName)
                            .append("lastName", motherLastName)
                            .append("id", motherId)
                            .append("birthDate", motherBirthDate)
                            .append("weight", motherWeight)
                            .append("height", motherHeight)
                            .append("babies", java.util.Arrays.asList(babyDoc)); 

        collection.replaceOne(com.mongodb.client.model.Filters.eq("id", motherId), doc, new com.mongodb.client.model.ReplaceOptions().upsert(true));

        javax.swing.JOptionPane.showMessageDialog(this, "Mother and Baby data successfully uploaded to the cloud!", "Save", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter valid numbers. Check weights and heights for Mother (decimals with '.') and Baby (integers).", "Format Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error saving Mother: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveMotherActionPerformed

