    private void updateBalanceDisplay() {
        lblBalance.setText(String.format("Balance: $%.2f", currentBalance));
        lblStatus.setText("Status: " + (isOpen ? "OPEN" : "CLOSED"));
    }

    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        String input = JOptionPane.showInputDialog(this, "Enter initial amount:");
        if (input != null) {
            currentBalance = Double.parseDouble(input);
            isOpen = true;
            updateBalanceDisplay();
            outputArea.append("Cash opened with $" + currentBalance + "\n");
        }
    }//GEN-LAST:event_btnOpenActionPerformed

    private void btnCloseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCloseActionPerformed
        if (!isOpen) {
            JOptionPane.showMessageDialog(this, "Cash is not open");
            return;
        }
        String input = JOptionPane.showInputDialog(this, "Enter physical count:");
        double physical = Double.parseDouble(input);
        double diff = physical - currentBalance;
        outputArea.append("Close - Expected: $" + currentBalance + " Physical: $" + physical + " Difference: $" + diff + "\n");
        isOpen = false;
        currentBalance = 0;
        updateBalanceDisplay();
        JOptionPane.showMessageDialog(this, "Cash closed");
    }//GEN-LAST:event_btnCloseActionPerformed

    private void btnIncomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncomeActionPerformed
        if (!isOpen) {
            JOptionPane.showMessageDialog(this, "Open cash first");
            return;
        }
        double amount = Double.parseDouble(txtAmount.getText());
        currentBalance += amount;
        updateBalanceDisplay();
        outputArea.append("Income: +$" + amount + " | " + txtDescription.getText() + "\n");
        txtAmount.setText("");
        txtDescription.setText("");
    }//GEN-LAST:event_btnIncomeActionPerformed

    private void btnExpenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExpenseActionPerformed
        if (!isOpen) {
            JOptionPane.showMessageDialog(this, "Open cash first");
            return;
        }
        double amount = Double.parseDouble(txtAmount.getText());
        if (amount > currentBalance) {
            JOptionPane.showMessageDialog(this, "Insufficient balance");
            return;
        }
        currentBalance -= amount;
        updateBalanceDisplay();
        outputArea.append("Expense: -$" + amount + " | " + txtDescription.getText() + "\n");
        txtAmount.setText("");
        txtDescription.setText("");
    }//GEN-LAST:event_btnExpenseActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        outputArea.append("Current balance: $" + currentBalance + " | Status: " + (isOpen ? "OPEN" : "CLOSED") + "\n");
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDiffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiffActionPerformed
        String input = JOptionPane.showInputDialog(this, "Enter physical count:");
        double physical = Double.parseDouble(input);
        double diff = physical - currentBalance;
        outputArea.append("Difference: $" + diff + "\n");
    }//GEN-LAST:event_btnDiffActionPerformed

