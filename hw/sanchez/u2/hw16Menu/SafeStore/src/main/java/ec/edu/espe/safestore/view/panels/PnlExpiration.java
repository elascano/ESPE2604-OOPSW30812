    private void loadSampleData() {
        tableModel = (DefaultTableModel) tblProducts.getModel();
        tableModel.addRow(new Object[]{1, "Paper Plate", "2025-03-15", 0, "Good", "No discount"});
        tableModel.addRow(new Object[]{2, "Plastic Cup", "2024-12-20", 0, "Good", "No discount"});
        tableModel.addRow(new Object[]{3, "Disposable Fork", "2024-11-10", 0, "Good", "No discount"});
        updateDaysLeft();
    }

    private void updateDaysLeft() {
        LocalDate today = LocalDate.now();
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            String expiryStr = tableModel.getValueAt(i, 2).toString();
            LocalDate expiry = LocalDate.parse(expiryStr);
            long daysLeft = ChronoUnit.DAYS.between(today, expiry);
            tableModel.setValueAt(daysLeft, i, 3);
            if (daysLeft < 0) {
                tableModel.setValueAt("EXPIRED", i, 4);
            } else if (daysLeft < 30) {
                tableModel.setValueAt("Expiring Soon", i, 4);
            } else {
                tableModel.setValueAt("Good", i, 4);
            }
        }
    }

    private void btnCheckAlertsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckAlertsActionPerformed
        updateDaysLeft();
        outputArea.append("\n=== EXPIRATION ALERTS ===\n");
        boolean hasAlerts = false;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            String status = tableModel.getValueAt(i, 4).toString();
            if (status.equals("Expiring Soon")) {
                outputArea.append("  " + tableModel.getValueAt(i, 1) + " - " + tableModel.getValueAt(i, 3) + " days left\n");
                hasAlerts = true;
            }
        }
        if (!hasAlerts) {
            outputArea.append("  No products expiring soon\n");
        }
        outputArea.append("\n");
    }//GEN-LAST:event_btnCheckAlertsActionPerformed

    private void btnCalculateDiscountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateDiscountActionPerformed
        updateDaysLeft();
        outputArea.append("\n=== PROGRESSIVE DISCOUNTS ===\n");
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            long daysLeft = (long) tableModel.getValueAt(i, 3);
            String discount;
            if (daysLeft <= 0) {
                discount = "50% (EXPIRED)";
            } else if (daysLeft <= 7) {
                discount = "30% (Last week)";
            } else if (daysLeft <= 15) {
                discount = "20% (Two weeks)";
            } else if (daysLeft <= 30) {
                discount = "10% (One month)";
            } else {
                discount = "No discount";
            }
            tableModel.setValueAt(discount, i, 5);
            if (!discount.equals("No discount")) {
                outputArea.append("  " + tableModel.getValueAt(i, 1) + ": " + discount + "\n");
            }
        }
        outputArea.append("\n");
    }//GEN-LAST:event_btnCalculateDiscountActionPerformed

    private void btnViewExpiredActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewExpiredActionPerformed
        updateDaysLeft();
        outputArea.append("\n=== EXPIRED PRODUCTS ===\n");
        boolean hasExpired = false;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            if (tableModel.getValueAt(i, 4).toString().equals("EXPIRED")) {
                outputArea.append("  " + tableModel.getValueAt(i, 1) + " - Expired on " + tableModel.getValueAt(i, 2) + "\n");
                hasExpired = true;
            }
        }
        if (!hasExpired) {
            outputArea.append("  No expired products\n");
        }
        outputArea.append("\n");
    }//GEN-LAST:event_btnViewExpiredActionPerformed

