    private void loadSampleData() {
        tableModel = (DefaultTableModel) tblReport.getModel();
        List<Product> products = reportController.getSlowMovingProducts();
        if (products.isEmpty()) {
            tableModel.addRow(new Object[]{101, "Paper Plate 25cm", 150, 50, 0, "Pending"});
            tableModel.addRow(new Object[]{102, "Plastic Cup 12oz", 200, 80, 0, "Pending"});
            tableModel.addRow(new Object[]{103, "Disposable Fork", 500, 100, 0, "Pending"});
            tableModel.addRow(new Object[]{104, "Paper Bag", 30, 50, 0, "Pending"});
        }
    }

    private void btnAnalyzeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalyzeActionPerformed
        outputArea.append("\n=== ANALYZING PRODUCTS ===\n");
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            int stock = Integer.parseInt(tableModel.getValueAt(i, 2).toString());
            int minStock = Integer.parseInt(tableModel.getValueAt(i, 3).toString());
            double rate = (double) stock / (minStock + 1);
            tableModel.setValueAt(String.format("%.2f", rate), i, 4);
            if (rate < 0.5) {
                tableModel.setValueAt("SLOW MOVING", i, 5);
                outputArea.append("  [SLOW] " + tableModel.getValueAt(i, 1) + " - Rate: " + String.format("%.2f", rate) + "\n");
            } else if (rate < 1.5) {
                tableModel.setValueAt("Normal", i, 5);
            } else {
                tableModel.setValueAt("Fast Moving", i, 5);
            }
        }
        outputArea.append("\n");
    }//GEN-LAST:event_btnAnalyzeActionPerformed

