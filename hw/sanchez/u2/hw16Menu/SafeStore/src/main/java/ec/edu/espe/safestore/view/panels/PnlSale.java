    private void loadProductsTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblProducts.getModel();
        model.setRowCount(0);
        List<Product> products = productController.getAllProducts();
        for (Product p : products) {
            model.addRow(new Object[]{p.getId(), p.getName(), p.getRetailPrice(), p.getStock()});
        }
    }

    private void newSale() {
        cartModel.setRowCount(0);
        total = 0;
        lblTotal.setText("Total: $0.00");
        txtCustomerName.setText("");
        String saleType = cbSaleType.getSelectedItem().toString().toLowerCase();
        String paymentMethod = cbPaymentMethod.getSelectedItem().toString().toLowerCase();
        saleController.startNewSale(currentSaleId, "Customer", saleType, paymentMethod);
        outputArea.append("New sale #" + currentSaleId + " started\n");
    }

    private void finishSale() {
        if (cartModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Cart is empty!");
            return;
        }
        String customerName = txtCustomerName.getText().trim();
        if (customerName.isEmpty()) customerName = "Walk-in Customer";
        double tax = total * 0.15;
        double grandTotal = total + tax;
        int confirm = JOptionPane.showConfirmDialog(this,
            "Customer: " + customerName + "\nSubtotal: $" + total + "\nVAT (15%): $" + tax + "\nTOTAL: $" + grandTotal + "\n\nConfirm sale?",
            "Confirm Sale", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            saleController.finalizeSale();
            outputArea.append("Sale #" + currentSaleId + " completed - Total: $" + grandTotal + "\n");
            currentSaleId++;
            newSale();
            JOptionPane.showMessageDialog(this, "Sale completed!");
        }
    }

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int row = tblProducts.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a product first");
            return;
        }
        try {
            String name = tblProducts.getValueAt(row, 1).toString();
            double price = Double.parseDouble(tblProducts.getValueAt(row, 2).toString());
            int stock = Integer.parseInt(tblProducts.getValueAt(row, 3).toString());
            int quantity = Integer.parseInt(txtQuantity.getText());
            if (quantity > stock) {
                JOptionPane.showMessageDialog(this, "Insufficient stock! Available: " + stock);
                return;
            }
            double subtotal = price * quantity;
            cartModel.addRow(new Object[]{name, quantity, price, subtotal});
            total += subtotal;
            lblTotal.setText(String.format("Total: $%.2f", total));
            outputArea.append("Added: " + name + " x" + quantity + "\n");
            txtQuantity.setText("");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter valid quantity");
        }
    }//GEN-LAST:event_btnAddActionPerformed

