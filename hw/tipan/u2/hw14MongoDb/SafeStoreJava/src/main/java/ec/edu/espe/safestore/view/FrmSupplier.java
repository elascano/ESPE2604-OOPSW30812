    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    // CONECTAR A MONGO
    MongoDatabase db = MongoConnection.getDatabase();
    MongoCollection<Document> collection = db.getCollection("suppliers");

    // CREAR JSON
    Document doc = new Document("id", txtId.getText())
            .append("name", txtName.getText())
            .append("phone", txtPhone.getText())
            .append("email", txtEmail.getText());

    // GUARDAR EN MONGO
    collection.insertOne(doc);

    // GUARDAR EN TABLA
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    model.addRow(new Object[]{
        txtId.getText(),
        txtName.getText(),
        txtPhone.getText(),
        txtEmail.getText()
    });

    // LIMPIAR CAMPOS
    txtId.setText("");
    txtName.setText("");
    txtPhone.setText("");
    txtEmail.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

