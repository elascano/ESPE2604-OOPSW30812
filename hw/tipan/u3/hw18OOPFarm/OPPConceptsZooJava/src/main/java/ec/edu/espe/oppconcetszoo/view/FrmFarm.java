    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    FarmAnimalDAO dao = new FarmAnimalDAO();

    int id = Integer.parseInt(txtId.getText());
    String breed = txtBreed.getText();
    float weight = Float.parseFloat(txtWeight.getText());

    String type = cmbAnimal.getSelectedItem().toString();

    dao.saveAnimal(id, breed, weight, type);

    // REGLAS DE NEGOCIO
    boolean young = weight < 100;
    boolean sale = weight >= 300;
    boolean food = weight < 50;

    javax.swing.JOptionPane.showMessageDialog(
            this,
            "Animal saved successfully!\n\n"
            + "Young Animal: " + young
            + "\nReady For Sale: " + sale
            + "\nNeeds More Food: " + food);

    txtId.setText("");
    txtBreed.setText("");
    txtWeight.setText("");
    cmbAnimal.setSelectedIndex(0);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
    FarmAnimalDAO dao = new FarmAnimalDAO();

    StringBuilder data = new StringBuilder();

    for (String animal : dao.getAnimals()) {
        data.append(animal).append("\n");
    }

    javax.swing.JOptionPane.showMessageDialog(
            this,
            data.toString());
    }//GEN-LAST:event_btnShowActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    float weight = Float.parseFloat(txtWeight.getText());

    FarmAnimal animal =
            new Cow(1, "Temp", new Date(), weight);

    String message = "";

    message += "Young Animal: "
            + animal.isYoungAnimal() + "\n";

    message += "Ready For Sale: "
            + animal.isReadyForSale() + "\n";

    message += "Needs More Food: "
            + animal.needsMoreFood();

    javax.swing.JOptionPane.showMessageDialog(this, message);
    }//GEN-LAST:event_jButton1ActionPerformed

