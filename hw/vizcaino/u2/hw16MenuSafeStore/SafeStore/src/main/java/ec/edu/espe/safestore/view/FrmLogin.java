    private void btnEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnterActionPerformed
    String user = txtUser.getText().trim();
    String password = String.valueOf(txtPassword.getPassword()).trim();
    String typeUser = ComboTOU.getSelectedItem().toString();

    if (user.equals("Manager")
            && password.equals("Manager")
            && typeUser.equals("Manager")) {

        FrmMenuManager menuManager = new FrmMenuManager();
        menuManager.setVisible(true);

        this.dispose();
    }

    else if (user.equals("Cashier")
            && password.equals("Cashier")
            && typeUser.equals("Cashier")) {

        FrmMenuCashier menuCashier = new FrmMenuCashier();
        menuCashier.setVisible(true);

        this.dispose();
    }

    else {

        JOptionPane.showMessageDialog(
                this,
                "Something went wrong, try again",
                "Login Error",
                JOptionPane.ERROR_MESSAGE);

    }
    }//GEN-LAST:event_btnEnterActionPerformed

